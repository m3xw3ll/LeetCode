def num_rook_captures(board):
    for i in range(8):
        for j in range(8):
            if board[i][j] == 'R':
                rx, ry = i, j

    out = 0
    for i, j in [[1, 0], [-1, 0], [0, 1], [0, -1]]:
        x, y = rx + i, ry + j
        while 0 <= x < 8 and 0 <= y < 8:
            if board[x][y] == 'p':
                out += 1
            if board[x][y] != '.':
                break
            x += i
            y += j
    return out


